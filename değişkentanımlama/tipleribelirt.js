// Farklı veri türlerinde değişkenler oluşturma
var str = "Merhaba"; // String
var num = 42; // Number
var bool = true; // Boolean -- true mu false mu
var undef; // tanımlanmamış
var boş = null; // Null
var obj = { isim: "Erdal", yaş: 20 }; // Object
var küme = [1, 2, 3, 4]; // Array 
var func = function () { return "Bu bir fonksiyon"; }; // funcion
// bunlar çıktılar
console.log("str: ", typeof str);
console.log("num: ", typeof num);
console.log("bool: ", typeof bool);
console.log("undef: ", typeof undef);
console.log("boş: ", typeof boş);
console.log("obj: ", typeof obj);
console.log("dizi: ", typeof küme);
console.log("func: ", typeof func);
